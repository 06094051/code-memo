package edu.hfut.mybatis.domain;

public class PersonInfo {
	private String name;
	private int year;

	public void setName(String name) {
		this.name = name;
	}

	public void setYear(int year) {
		this.year = year;
	}

	public String getName() {
		return name;
	}

	public int getYear() {
		return year;
	}

}
